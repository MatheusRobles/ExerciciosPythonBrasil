print("##Identificando Maior Numero##")
num1 = int(input("Digite um numero: \n"))
num2 = int(input("Digite um novo numero: \n"))

if num1 > num2:
    print("O primeiro numero é maior que o segundo.")
elif num1 == num2:
    print("Os numeros são iguais")
else:
    print("O segundo numero é maior que o primeiro")
